package contests.round1090;

import java.util.Scanner;

/**
 * 
 */
public class Maximimised {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int test = sc.nextInt();
		while (test-- > 0) {
			long n = sc.nextLong();
			if (n >= 0) {
				System.out.println(n);
			}
			else {
				System.out.println(n);
			}
		}
		sc.close();
	}

}
